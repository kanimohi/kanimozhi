package com.aequalis;
public class Switch{
    public static void main(String []args)
    {
        int num=5;
        switch(num){
            case 1:System.out.println("jan");
            break;
            case 2:System.out.println("feb");
            break;
            case 3:System.out.println("mar");
            break;
            case 4:System.out.println("april");
            break;
            default:System.out.println("*");
        }
    }
}